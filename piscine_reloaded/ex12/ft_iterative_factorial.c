int	ft_iterative_factorial(int nb)
{
	int	i;
	int	x;

	i = 1; // 3! = 3 * 2 * 1 = 6
	x = 1; // nb = 3;
	if ((nb < 0) || (nb > 12)) //falso
		return (0);
	else if (nb == 0) //falso
		return (1);
	while (i <= nb) // 1 <= 3
	{
		x = x * i; // 1 = 1 * 1
		i++;      //  2 = 1 * 2
	}            // 6 = 2 * 3
		return (x);// 24 = 6 * 4
}
